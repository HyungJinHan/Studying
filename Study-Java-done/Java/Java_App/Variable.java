
public class Variable {

	public static void main(String[] args) {
		
		// integer(int) : 정수 (-2, -1, 0, 1, 2)
		int a = 1; // 1은 integer(정수)이므로 변수 지정 시, int 사용
		System.out.println(a);
		
		// Real Number(double) : 실수 (1.1, 1.2, 1.0)
		double b = 1.5; // 1.5는 Real Number(실수)이므로 변수 지정 시, double 사용
		System.out.println(b);
		
		// String : 문자열
		String c = "Hello HHJ!";
		System.out.println(c); // Hello HHJ!는 String(문자열)이므로 변수 지정 시, String 사용
		
	}

}

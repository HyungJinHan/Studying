
public class Variable_Datatype {

	public static void main(String[] args) {
		
		// 1. 논리형
		boolean name1 = true;
		System.out.println(name1); // true
		
		// 2. 문자형
		char name2 = 'a'; // 문자가 하나 이상일 경우 문자열로 취급 (''은 문자)
		System.out.println(name2); // a
		
		String name6 = "abc"; // ""은 문자열
		System.out.println(name6); // abc
		
		// 3. 정수형
		int name3 = 8;
		System.out.println(name3); // 8
		
		// 4. 실수형
		double name4 = 3.14;
		System.out.println(name4); // 3.14
		
		float name5 = 1.5f; // float 지정을 위해 값 뒤에 f 입력
		System.out.println(name5); // 1.5

	}

}
